package cn.finder.wae.queryer.handleclass.sdk;

public class SdkParam {

	private String interfaceKey;
	
	private String lang;
	
	private String funcName;
	
	private String nameSpace;
	
	private String retModelName;
	
	private String reqType;

	public String getInterfaceKey() {
		return interfaceKey;
	}

	public void setInterfaceKey(String interfaceKey) {
		this.interfaceKey = interfaceKey;
	}

	public String getLang() {
		return lang;
	}

	public void setLang(String lang) {
		this.lang = lang;
	}

	public String getFuncName() {
		return funcName;
	}

	public void setFuncName(String funcName) {
		this.funcName = funcName;
	}

	public String getNameSpace() {
		return nameSpace;
	}

	public void setNameSpace(String nameSpace) {
		this.nameSpace = nameSpace;
	}

	public String getRetModelName() {
		return retModelName;
	}

	public void setRetModelName(String retModelName) {
		this.retModelName = retModelName;
	}

	public String getReqType() {
		return reqType;
	}

	public void setReqType(String reqType) {
		this.reqType = reqType;
	}
	
	
	
}
